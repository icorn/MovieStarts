import Foundation
import UIKit


extension UIColor {
    
    class func darkTürkisColor() -> UIColor {
        return UIColor(red: 30.0/255.0, green: 178.0/255.0, blue: 175.0/255.0, alpha: 1.0)
    }

    class func lightTürkisColor() -> UIColor {
        return UIColor(red: 24.0/255.0, green: 190.0/255.0, blue: 190.0/255.0, alpha: 1.0)
    }

    class func lightGrayBackgroundColor() -> UIColor {
        return UIColor(white: 246.0/255.0, alpha: 1.0)
    }
    
}
